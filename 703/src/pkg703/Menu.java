    private void MenuTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_MenuTableMouseClicked
        DefaultTableModel model = (DefaultTableModel)MenuTable.getModel();
        int getSelectedRow = MenuTable.getSelectedRow();
        
        String id = model.getValueAt(getSelectedRow, 0).toString();
        String itemName = model.getValueAt(getSelectedRow, 1).toString();
        String price = model.getValueAt(getSelectedRow, 2).toString();
        
        try {
            new ShowMenu(id , itemName , price).setVisible(true);
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_MenuTableMouseClicked

