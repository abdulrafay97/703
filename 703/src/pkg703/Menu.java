    public void ShowDataInTheMenuTable() throws SQLException, ClassNotFoundException{
        Connection con = getDbConnection();
        
        String sql = "Select * from menu";
        PreparedStatement pstmt = con.prepareStatement(sql);
        ResultSet rs = pstmt.executeQuery();
        MenuTable.setModel(DbUtils.resultSetToTableModel(rs));
    }
    
    public void searchMenu() throws SQLException, ClassNotFoundException{
        Connection con = getDbConnection();
        
        String stf = searchtxt.getText();
        
        String qry = "Select * From menu where Item_Name LIKE '%"+stf+"%' ";
        PreparedStatement pstmt = con.prepareStatement(qry);
        ResultSet rs = pstmt.executeQuery();
        MenuTable.setModel(DbUtils.resultSetToTableModel(rs));
    }

